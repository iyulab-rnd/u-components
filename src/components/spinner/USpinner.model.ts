/**
 * Represents the configuration options for a spinner component.
 */
export interface USpinnerModel {
  /**
   * The size of the spinner. The value is css syntax.
   */
  size?: string;
  
  /**
   * The width of the spinner.
   */
  width?: string;
  
  /**
   * The color of the spinner indicator.
   */
  indicatorColor?: string;
  
  /**
   * The color of the spinner track.
   */
  trackColor?: string;
}